package kr.co.link.vo;

public class BandHistory {

	private Integer no;
	private Integer bandNo;
	private String userId;
	private String masterYn;
	private Integer bandCount;
	private String joinYn;

	public Integer getNo() {
		return no;
	}
	public void setNo(Integer no) {
		this.no = no;
	}
	public Integer getBandNo() {
		return bandNo;
	}
	public void setBandNo(Integer bandNo) {
		this.bandNo = bandNo;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getMasterYn() {
		return masterYn;
	}
	public void setMasterYn(String masterYn) {
		this.masterYn = masterYn;
	}
	public Integer getBandCount() {
		return bandCount;
	}
	public void setBandCount(Integer bandCount) {
		this.bandCount = bandCount;
	}
	public String getJoinYn() {
		return joinYn;
	}
	public void setJoinYn(String joinYn) {
		this.joinYn = joinYn;
	}
	
}
