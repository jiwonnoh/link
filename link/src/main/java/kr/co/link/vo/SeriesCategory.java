package kr.co.link.vo;

public class SeriesCategory {

	private Integer no;
	private String name;

	public SeriesCategory() {
	}

	public Integer getNo() {
		return no;
	}

	public void setNo(Integer no) {
		this.no = no;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
}
